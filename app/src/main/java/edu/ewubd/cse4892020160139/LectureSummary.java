// Change the class name and attributes according to the fields of Lectur Summary

public class LectureSummary {
    String id = "";
    String name = "";
    String course = "";
    String type = "";
    String date = "";
    String lecture = "";
    String topic = "";
    String summary = "";


    public LectureSummary(String id, String name, String course, String type,String date,String lecture,String topic,String summary){
        this.id = id;
        this.name = name;
        this.course = course;
        this.type = type;
        this.date = date;
        this.lecture = lecture;
        this.topic = topic;
        this.summary = summary;
    }
}
